/* Questão 09 da lista de Repetição: https://wiki.python.org.br/EstruturaDeRepeticao  */

public class RepQ09 {
    
    public static void main(String[] args) {
        
        //Uso do for para iterar somente pelos números impares. Observe a iteração i+=2
        for (int i = 1; i <= 50; i+=2) {
            
            System.out.println(i);
        }
    }
}
